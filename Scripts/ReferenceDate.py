ReferenceDate = datetime.datetime.now()
def GetDate(dt):
	return dt - datetime.timedelta(hours=dt.hour,minutes=dt.minute,seconds=dt.second,microseconds=dt.microsecond)
ReferenceDate = GetDate(ReferenceDate)

